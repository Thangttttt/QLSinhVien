    TaiKhoanDAO dao=new TaiKhoanDAO();
    
    boolean checkTenDN(){
        String tenDnDtb="";
        tenDnDtb = dao.selectByIdT(txtTenDangNhap.getText());
        try{
            if(tenDnDtb.equalsIgnoreCase("")){
                return false;
            }
        }catch(Exception e){
            MsgBox.alert(this, "Sai tên tài khoản");
            return false;
        }
        return true;
    }
    boolean checkMKd(){
        String MKDnDtb="";
        MKDnDtb = dao.selectByIdMK(txtTenDangNhap.getText());
        try{
            if(!MKDnDtb.equalsIgnoreCase(txtMatKhau.getText())){
                MsgBox.alert(this, "Sai mật khẩu hiện tại");
                return false;
            }
        }catch(Exception e){
            return false;
        }
        return true;
    }
    boolean checkmktrung(){
        if(txtMatKhauMoi.getText().equals(txtMatKhau.getText())){
            MsgBox.alert(this, "mật khẩu mới trùng với mật khẩu cũ");
            return false;
        }
        return true;
    }
    void update(){
        if(checkform() && checkTenDN() && checkMKd() &&checkmktrung()){
            String UPDATE_SQL = "Update taikhoan SET matkhau=? where tendangnhap=?";
            try{
                JdbcHelper.update(UPDATE_SQL,txtMatKhauMoi.getText(),txtTenDangNhap.getText());
                MsgBox.alert(this, "cập nhật thành công");
            }catch(Exception e){
                MsgBox.alert(this, "Cập nhật thất bại");
            }
        }
    }
    boolean checkform(){
        if(txtTenDangNhap.getText().equals("") ){
            MsgBox.alert(this, "Không được để trống Tên đăng nhập");
            return false;
        }if(txtMatKhau.getText().equals("")){
            MsgBox.alert(this, "Không được để trống điểm mật khẩu");
            return false;
        }if(txtMatKhauMoi.getText().equals("")){
            MsgBox.alert(this, "Không được để trống mật khẩu mới");
            return false;}
        if(txtXacNhanMatKhauMoi.getText().equals("")){
            MsgBox.alert(this, "Không được để trống xác nhạn mật khẩu");
            return false;
        }if(!txtMatKhauMoi.getText().equals(txtXacNhanMatKhauMoi.getText())){
            MsgBox.alert(this, "Mật khẩu xác nhận không chính xác ");
            return false;
        }else{
            return true;
        }
    }
    
    
