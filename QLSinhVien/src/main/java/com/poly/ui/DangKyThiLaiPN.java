    void init() {
//        this.fillTable();
        this.row = -1;
        fillComboBoxKhoa();
        fillTableDanhSachSinhVien();
    }

    void insert() {
        if (checkform()) {
            DangKyThiLai dk = getForm();
            try {
                dao.insert(dk);
                this.fillTableDanhSachSinhVien();
                this.clearForm();
                MsgBox.alert(this, "Đăng Ký thành công");
//                new ThongTinChuyenKhoan(this, true).setVisible(true);
            } catch (Exception e) {
                MsgBox.alert(this, "Đăng Ký thất bại");
                e.printStackTrace();
            }
        }
    }

    void fillComboBoxKhoa() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhoa.getModel();
        model.removeAllElements();
        List<Khoa> list = khoadao.selectAll();
        for (Khoa k : list) {
            model.addElement(k);
        }
        this.fillComboBoxMonHoc();
//        this.fillComboBoxLop();
    }

    void fillComboBoxMonHoc() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboMonHoc.getModel();
        model.removeAllElements();
        Khoa khoa = (Khoa) cboKhoa.getSelectedItem();
        if (khoa != null) {
            List<MonHoc> list = monhocdao.selectByKhoaM(khoa.getMaKhoa());
            for (MonHoc mh : list) {
                model.addElement(mh);
            }
        }
    }

    void clearForm() {
        DangKyThiLai dk = new DangKyThiLai();
        this.setForm(dk);
//        this.setIDefault();
        this.row = -1;
    }

    void setForm(DangKyThiLai dk) {
        txtMaSV.setText(dk.getMaSV());
        cboKhoa.setSelectedItem(dk.getMaKhoa());
        txtLop.setText(dk.getMaLop());
        cboMonHoc.setSelectedItem(dk.getMaMH());
        txtHovaTen.setText(dk.getHoTen());

    }

    DangKyThiLai getForm() {
        DangKyThiLai dk = new DangKyThiLai();
        dk.setMaSV(txtMaSV.getText());
        dk.setMaKhoa(cboKhoa.getSelectedItem().toString());
        dk.setMaLop(txtLop.getText());
        dk.setMaMH(cboMonHoc.getSelectedItem().toString());
        dk.setHoTen(txtHovaTen.getText());
        dk.setTrangThai("Chưa Thanh Toán");
        return dk;
    }

    private void HienThi() {
        try {
            SinhVienDAO dao = new SinhVienDAO();
            SinhVien sv = dao.selectById(txtMaSV.getText());
            if (sv != null) {
                txtLop.setText(sv.getMaLop());
                txtHovaTen.setText(sv.getHoTen());

            }
        } catch (Exception e) {
        }
    }

    void fillTableDanhSachSinhVien() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        //KhoaHoc khoaHoc = (KhoaHoc) cboKhoaHoc.getSelectedItem();
//        if (khoaHoc != null) {
        List<DangKyThiLai> list = dao.selectAll();
        for (DangKyThiLai dk : list) {
            Object[] row = {
                dk.getMaSV(),
                dk.getMaKhoa(),
                dk.getMaLop(),
                dk.getMaMH(),
                dk.getHoTen(),
                dk.getTrangThai()
            };
            model.addRow(row);
        }
//        }
    }

    boolean checkform() {
        if (txtMaSV.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống mã sinh viên");
            return false;
        } else {
            return true;
        }
    }

    private void timKiem() {
        load();
    }

    void load() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<DangKyThiLai> list = dao.selectByKeyword(keyword);
            for (DangKyThiLai dk : list) {
                Object[] row = {
                    dk.getMaSV(),
                    dk.getMaKhoa(),
                    dk.getMaLop(),
                    dk.getMaMH(),
                    dk.getHoTen(),
                    dk.getTrangThai(),};
                model.addRow(row);
            };
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu người học!");
            e.printStackTrace();
        }
    }

