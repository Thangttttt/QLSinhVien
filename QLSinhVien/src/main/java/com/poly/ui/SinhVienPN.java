    public void autoSizeColumns(Workbook workbook) {
        int numberOfSheets = workbook.getNumberOfSheets();
        for (int i = 0; i < numberOfSheets; i++) {
            Sheet sheet = workbook.getSheetAt(i);
            if (sheet.getPhysicalNumberOfRows() > 0) {
                Row row = sheet.getRow(sheet.getFirstRowNum());
                Iterator<Cell> cellIterator = row.cellIterator();
                while (cellIterator.hasNext()) {
                    Cell cell = cellIterator.next();
                    int columnIndex = cell.getColumnIndex();
                    sheet.autoSizeColumn(columnIndex);
                    int currentColumnWidth = sheet.getColumnWidth(columnIndex);
                    sheet.setColumnWidth(columnIndex, (currentColumnWidth + 600));
                }
            }
        }
    }
    
    boolean checkform() {
        if (txtMaSV.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống mã Sinh viên");
            return false;
        }
        if (txtHovaTen.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống họ và tên");
            return false;
        }
        if (txtNgaySinh.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống ngày sinh");
            return false;
        }
        if (txtEmail.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống email");
            return false;
        }
        if (txtDiaChi.getText().equals("")) {
            MsgBox.alert(this, "Không được để trống địa chỉ");
            return false;
        } 
        
        String email = txtEmail.getText();
        if(!email.matches("\\w+@\\w+(\\.\\w+){1,2}")){
            MsgBox.alert(this, "Không đúng định dạng Email");
            return false;
        }
        
        String ma = txtMaSV.getText();
        if (ma.length() > 10) {
            MsgBox.alert(this, "Mã không được lớn hơn 10 kí tự");
            return false;
        }
        
        else{
            return true;
        }
    }
    
    boolean checkMasv() {
        List<SinhVien> listsv = dao.selectAll();
        for (SinhVien sv : listsv) {
            if (sv.getMaSV().equals(txtMaSV.getText())) {
                MsgBox.alert(this, "Mã sinh viên này đã tồn tại");
                return false;
            }
        }
        return true;
    }
