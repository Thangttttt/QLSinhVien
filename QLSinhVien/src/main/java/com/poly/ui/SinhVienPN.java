    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
        model.setRowCount(0);
        try {
            Lop loP = (Lop) cboLop.getSelectedItem();
            if (loP != null) {
                List<SinhVien> list = dao.selectByLop(loP.getMaLop());

                for (int i = 0; i < list.size(); i++) {
                    SinhVien sinhVien = list.get(i);
                    Object[] row = {
                        sinhVien.getMaSV(),
                        sinhVien.getHoTen(),
                        XDate.toString(sinhVien.getNgaySinh()),
                        sinhVien.isGioiTinh() ? "Nam" : "Nữ",
                        sinhVien.getDiaChi(),
                        sinhVien.getMaLop(),
                        sinhVien.getHinh(),
                        sinhVien.getEmail()
                    };

                    model.addRow(row);
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Lỗi truy cập dữ liệu!");
        }
    }

